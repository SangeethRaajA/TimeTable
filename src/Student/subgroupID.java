    private void DeleteBtnGroupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBtnGroupActionPerformed
        // TODO add your handling code here:
        int row = subgroupID_table.getSelectedRow();
        String id = (subgroupID_table.getModel().getValueAt(row, 0).toString());

        String sql = "delete from subgroupIDNew where subgroupID='"+id+"'";

        int opt = JOptionPane.showConfirmDialog(null,"Are you sure to delete?","Delete", JOptionPane.YES_NO_OPTION);
        if(opt==0){
            try{
                pst = con.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Deleted Successfully");
                // dispose();

            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            UpDateTable();
        }
    }//GEN-LAST:event_DeleteBtnGroupActionPerformed

    private void AddBtnGroupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnGroupActionPerformed
        // TODO add your handling code here:
        String sql = "INSERT INTO subgroupIdNew(subgroupID) VALUES(?)";
        //String sql = "INSERT INTO check(name,age)"

        try{
            pst = con.prepareStatement(sql);
            String groupID = GroupIDComboBox.getSelectedItem().toString();
            String sub = SubgroupComboBox.getSelectedItem().toString();
            pst.setString(1,groupID+"."+sub);

            pst.execute();
            JOptionPane.showMessageDialog(null, "Inserted Successfully");
            // dispose();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        UpDateTable();
    }//GEN-LAST:event_AddBtnGroupActionPerformed

