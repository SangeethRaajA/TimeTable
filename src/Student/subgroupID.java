    private void UpDateTable(){
        String sql = "SELECT * FROM subgroupIDNew";
        try{
            pst = con.prepareStatement(sql);
            result = pst.executeQuery();
            subgroupID_table.setModel(DbUtils.resultSetToTableModel(result));
           // System.out.println(result);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void UpDateComboGroupID(){
        String sql = "SELECT * FROM groupIdNew";
        try{
            pst = con.prepareStatement(sql);
            result = pst.executeQuery();
            while(result.next()){
                GroupIDComboBox.addItem(result.getString("groupID"));
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void UpDateComboSubgroup(){
        String sql = "SELECT * FROM subgroup";
        try{
            pst = con.prepareStatement(sql);
            result = pst.executeQuery();
            while(result.next()){
                SubgroupComboBox.addItem(result.getString("subgroup_name"));
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

