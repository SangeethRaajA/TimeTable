    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        // TODO add your handling code here:
        
        String sql = "INSERT INTO year(year,semester) VALUES(?,?)";
        //String sql = "INSERT INTO check(name,age)"

        try{
            pst = con.prepareStatement(sql);
            pst.setString(1, "Y"+year_lable.getText());
            pst.setString(2, "S"+semester_lable.getText());

            pst.execute();
            JOptionPane.showMessageDialog(null, "Inserted Successfully");
            // dispose();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        UpDateTable();
    }//GEN-LAST:event_AddBtnActionPerformed

    private void EditBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditBtnActionPerformed
        // TODO add your handling code here:
        int opt = JOptionPane.showConfirmDialog(null,"Are you sure to edit?","Edit", JOptionPane.YES_NO_OPTION);
        if(opt==0){
        try{
            int row = year_table.getSelectedRow();
            String id = (year_table.getModel().getValueAt(row, 0).toString());
            String value1 = year_lable.getText();
            String value2 = semester_lable.getText();
            
            String sql = "update year set year='"+value1+"',semester='"+value2+"' where yearID='"+id+"'";


            pst = con.prepareStatement(sql);
            pst.execute();

            JOptionPane.showMessageDialog(null, "Updated Successfully");
            // dispose();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        UpDateTable();
        }
    }//GEN-LAST:event_EditBtnActionPerformed

    private void DeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBtnActionPerformed
        // TODO add your handling code here:

        String sql = "delete from year where year=?";
        //String sql = "INSERT INTO check(name,age)"
        
        int opt = JOptionPane.showConfirmDialog(null,"Are you sure to delete?","Delete", JOptionPane.YES_NO_OPTION);
        if(opt==0){
            try{
                pst = con.prepareStatement(sql);
                pst.setString(1, year_lable.getText());
                pst.execute();
                JOptionPane.showMessageDialog(null, "Deleted Successfully");
                // dispose();

            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            UpDateTable();
        }
    }//GEN-LAST:event_DeleteBtnActionPerformed

    private void year_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_year_tableMouseClicked
        // TODO add your handling code here:
        try{
            int i = year_table.getSelectedRow();
            String Table_click=(year_table.getModel().getValueAt(i, 0).toString());

            String sql = "select * from year where yearID='"+Table_click+"'"; 
            pst = con.prepareStatement(sql);
            result=pst.executeQuery();
            if(result.next()){
                String add1 = result.getString("year");
                String add2 = result.getString("semester");
                year_lable.setText(add1);
                semester_lable.setText(add2);
            }

            }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_year_tableMouseClicked

