    private void e_add_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_e_add_btnActionPerformed
        // TODO add your handling code here:
          if(e_add_name.getText().isEmpty() || e_add_id.getText().isEmpty() || e_add_facultry.getText().isEmpty() || e_add_department.getText().isEmpty() || e_add_center.getText().isEmpty() || e_add_building.getText().isEmpty()){
               String message = "All Field Should be Filled!";
                JOptionPane.showMessageDialog(new JFrame(), message, "Dialog",
                JOptionPane.ERROR_MESSAGE);

        }
        else{
        String sql = "INSERT INTO lecture1(name,emp_id,facultry,department,centre,building,level,rank) VALUES(?,?,?,?,?,?,?,?)";
       
   
           
        try{
            pst = con.prepareStatement(sql);
            pst.setString(1, e_add_name.getText());
            pst.setString(2, e_add_id.getText());
            pst.setString(3, e_add_facultry.getText());
            pst.setString(4, e_add_department.getText());
            pst.setString(5, e_add_center.getText());
            pst.setString(6, e_add_building.getText());
            pst.setString(7, e_add_level.getSelectedItem().toString());
            pst.setString(8, e_add_rank.getText());
            
            pst.execute();
            JOptionPane.showMessageDialog(null, "Inserted Successfully");
            dispose();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
       }
    }//GEN-LAST:event_e_add_btnActionPerformed

    private void e_add_idKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_e_add_idKeyPressed
        // TODO add your handling code here:
         int key = evt.getKeyCode();
        
        if((key>=evt.VK_0 && key<=evt.VK_9) || (key>=evt.VK_NUMPAD0 && key<=evt.VK_NUMPAD9) || (key==KeyEvent.VK_BACK_SPACE)){
            e_add_id.setEditable(true);
            e_add_id.setBackground(Color.white);
        }
        else{
            e_add_id.setEditable(false);
            e_add_id.setBackground(Color.red);
        }
    }//GEN-LAST:event_e_add_idKeyPressed

    private void e_add_levelItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_e_add_levelItemStateChanged
        // TODO add your handling code here:
        
        String rank = "1";
        
        String Level = e_add_level.getSelectedItem().toString();
        System.out.println(Level);
        switch(Level){
        
            case "Professor":{
                rank="1";
            }
            break;
            case "Assistant Professor":{
                rank="2";
            }
            
            break;
            case "Lecturer":{
                rank="5";
            }
            break;
            case "Instructors":{
                rank="7";
            }
            break;
            case "Senior Lecturer":{
                rank="4";
            }
            break;
            
            case "Assistant Lecturer":{
                rank="6";
            }
            break;
            default:{
                rank="3";
            }
        };
        String EmpID = e_add_id.getText().toString();
        
        
        e_add_rank.setText(rank+"."+EmpID);
        
    }//GEN-LAST:event_e_add_levelItemStateChanged

