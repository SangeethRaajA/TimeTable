    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql = "INSERT INTO location(buildingname, floor, roomname, roomtype, nonavailabletime) VALUES(?,?,?,?,?)";
        try{
            pst = conn.prepareStatement(sql);
            
            pst.setString(1, buildingname.getText());
            pst.setString(2, floor.getValue().toString());
            pst.setString(3, roomname.getText());
            String type;
            type = roomtype.getSelectedItem().toString();
            pst.setString(4, type);
            String time;
            time = nonavailabletime.getSelectedItem().toString();
            pst.setString(5, time);
            
             if(buildingname.getText().isEmpty() || roomname.getText().isEmpty()){
               String message = "All Field Should be Filled!";
                JOptionPane.showMessageDialog(new JFrame(), message, "Dialog",
                JOptionPane.ERROR_MESSAGE);

        }
        else{
            
            pst.execute();
            JOptionPane.showMessageDialog(null, "Inserted Successfully");
             retrievelocation retrieveLocation = new retrievelocation();
             retrieveLocation.setVisible(true);
             retrieveLocation.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            }
           
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

