    private void Add1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Add1ActionPerformed
        // TODO add your handling code here:
         try{
           //Class.forName("com.mysql.jdbc.Driver");
           
          // Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/tms","root","");
           
           String query="INSERT INTO workingdays(semID,dayNo,wDays,wTimePerDay,batch) VALUES(?,?,?,?,?)";
           PreparedStatement pst = conn.prepareStatement(query);
          pst.setString(1,semID.getText());
//          pst.setString(2,empName.getText());
          String daysNo;
          daysNo=dayNo.getSelectedItem().toString();
          pst.setString(2,daysNo);
          String wDays="";
          if(Monday.isSelected()){
              wDays+=Monday.getText()+" ";
          }
          if(Tuesday.isSelected()){
              wDays+=Tuesday.getText()+" ";
          }
          if(Wednesday.isSelected()){
              wDays+=Wednesday.getText()+" ";
          }
          if(Thursday.isSelected()){
              wDays+=Thursday.getText()+" ";
          }
          if(Friday.isSelected()){
              wDays+=Friday.getText()+" ";
          }
          if(Saturday.isSelected()){
              wDays+=Saturday.getText()+" ";
          }
          if(Sunday.isSelected()){
              wDays+=Sunday.getText()+" ";
          }
          pst.setString(3,wDays);
          pst.setString(4,wTimePerDay.getText());
          //
          String batches;
          batches=batch.getSelectedItem().toString();
          pst.setString(5,batches);
          
          
          
          pst.executeUpdate();
          JOptionPane.showMessageDialog(null,"Inserted Sucessfully!");
          
           
        }catch(Exception e ){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_Add1ActionPerformed

